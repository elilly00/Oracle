SELECT EMP_ID, EMP_NAME, EXTRACT(YEAR FROM  SYSDATE)-(SUBSTR(EMP_NO, 1, 2)+1900) 나이,
       DEPT_TITLE, JOB_NAME
FROM EMPLOYEE
