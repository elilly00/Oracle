-- 3. 이름에 '형'이 들어가는 사원의 사원코드, 사원명, 직급 조회
-- [오라클 구문]
SELECT EMP_ID, EMP_NAME, JOB_NAME
FROM EMPLOYEE E, JOB J
WHERE E.JOB_CODE = J.JOB_CODE
      AND EMP_NAME LIKE '%형%';

-- [ANSI 표준 구문]
SELECT EMP_ID, EMP_NAME, JOB_NAME
FROM EMPLOYEE
    JOIN JOB USING (JOB_CODE)
